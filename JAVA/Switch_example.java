package com.ust;

public class Switch_example {
	public static void main(String arg[]){
		String str="Green1";
		switch(str) {
		case "Red":
			System.out.println("i'm in red");
			System.out.println("*");
		default :
			System.out.println("default");
			break;
		case "Green":
			System.out.println("i'm in green");
			System.out.println("*");
			break;
		case "orange":
			System.out.println("i'm in orange");
			System.out.println("*");
			break;
	
		
		}
	}

}
