package com.ust;

class Switch_example4 {
	public static void main(String[] args) {
		int expression=2;
		//switch statement to check size
		switch(expression) {
		case 1:
			System.out.println("Case 1");
			//matching case
		case 2:
			System.out.println("case 2");
		case 3:
			System.out.println("case 3");
		default:
			System.out.println("Default case");
		}
	}

}
